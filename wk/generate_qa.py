def generate_qa(client, prompt_config):
    """1カテゴリ分のQ&Aを生成"""
    print(f"\n{'='*50}")
    print(f"生成中: {prompt_config['category']} ({prompt_config['count']}問)")
    print(f"{'='*50}")

    try:
        message = client.messages.create(
            model=MODEL,
            max_tokens=8000,
            messages=[{"role": "user", "content": prompt_config["prompt"]}]
        )

        raw = message.content[0].text.strip()

        # JSONの抽出（余計なテキストを除去）
        start = raw.find("[")
        end   = raw.rfind("]") + 1
        if start == -1 or end == 0:
            print(f"  ⚠️ JSON見つからず: {raw[:100]}")
            return []

        json_str = raw[start:end]
        data = json.loads(json_str)

        # カテゴリとIDを付与
        for i, item in enumerate(data):
            item["category"] = prompt_config["category"]
            item["id"]       = f"{prompt_config['id']}_{i+1:03d}"

        print(f"  ✅ {len(data)}問 生成完了")
        return data

    except json.JSONDecodeError as e:
        print(f"  ❌ JSONパースエラー: {e}")
        print(f"  raw: {raw[:200]}")
        return []
    except Exception as e:
        print(f"  ❌ エラー: {e}")
        return []


def main():
    print("=" * 60)
    print("  トレーラーハウス Q&A 500問生成")
    print(f"  開始時刻: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print("=" * 60)

    client = anthropic.Anthropic(api_key=API_KEY)

    all_qa = []

    # 元データの50問を追加
    print("\n[元データ] 既存Q&Aを追加中...")
    for i, item in enumerate(BASE_QA):
        item["id"] = f"base_{i+1:03d}"
        all_qa.append(item)
    print(f"  ✅ {len(BASE_QA)}問 追加完了")

    # 7カテゴリを順番に生成
    for config in PROMPTS:
        result = generate_qa(client, config)
        all_qa.extend(result)

        # 途中経過を保存（クラッシュ対策）
        save_json(all_qa, OUTPUT)
        print(f"  💾 中間保存: 現在合計 {len(all_qa)} 問")

        # API レート制限対策
        time.sleep(3)

    # 最終保存
    save_json(all_qa, OUTPUT)

    print("\n" + "=" * 60)
    print(f"  ✅ 完了！ 合計 {len(all_qa)} 問")
    print(f"  📄 保存先: {OUTPUT}")
    print(f"  終了時刻: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print("=" * 60)

    # カテゴリ別集計
    print("\n【カテゴリ別集計】")
    categories = {}
    for item in all_qa:
        cat = item.get("category", "不明")
        categories[cat] = categories.get(cat, 0) + 1
    for cat, count in categories.items():
        print(f"  {cat}: {count}問")


def save_json(data, filename):
    with open(filename, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)


if __name__ == "__main__":
    main()
